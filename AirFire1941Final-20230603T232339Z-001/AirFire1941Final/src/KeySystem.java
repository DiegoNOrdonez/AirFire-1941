
public class KeySystem {
	int left,right,up,down = 0;
	int space=0;
	public boolean isLeft() {
		return left!=0;
	}
	public boolean isRight() {
		return right!=0;
	}
	public boolean isUp() {
		return up!=0;
	}
	public boolean isDown() {
		return down!=0;
	}
	public boolean isSpace() {
		return space!=0;
	}
}
